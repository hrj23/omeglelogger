<h3>Omegle Logger</h3>
<p>Below is a list of conversations that were obtained from <a href="http://www.omegle.com">Omegle</a>. If you haven't heard of Omegle yet, it's a way to talk to strangers on the internet. You just visit the site, click a button and are joined to some other random stranger on the internet for a completely anonymous chat. We opened two simultaneous conversations using Omegle. We then relayed the two strangers' messages back and forth to each other as if they were talking to each other. They didn't know we were in the middle of their conversation logging it. :) Oh yeah, and we don't relay spam messages.</p>
